// Operadores Condicionais

// ==   ->  Operador de igualdade
// !=   ->  Operador de diferença
// >    ->  Maior que
// >=   ->  Maior ou igual
// <    ->  Menor que
// <=   ->  Menor ou igual

// Exercício 3:
let a = 1;
let b = 2;
console.log(a >= b);
console.log(a <= b);
console.log(a != b);
console.log(a == b);

